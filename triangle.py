def area(a, b, c):
    #Принимает стороны треугольника а, b, c соответственно, возвращает площадь
    #При а = 3, b = 2, c = 5, возвращает 5
    return (a + b + c) / 2


def perimeter(a, b, c):
    #Принимает стороны треугольника a, b, c, возрвщает периметр
    #При значениях 1, 2, 3 вернёт 6
    return a + b + c
