def area(a, h): 
    '''Получает длину стороны треугольника a (int) и длину высоты треугольника h (int) и высчитывает площадь этого треугольника(полупроизведение стороны на высоту)'''
    return a * h / 2 

def perimeter(a, b, c): 
    '''Получает длины сторон треугольника a (int) и b (int) и с (int) высчитывает периметр этого треугольника(сумма всех сторон)'''
    return a + b + c 
