import math


def area(r):
    '''Получает радиус круга r (int) и высчитывает площадь этого круга(пи на радиус в квадрате)'''
    return math.pi * r * r


def perimeter(r):
    '''Получает радиус круга r (int) и высчитывает периметр этого круга(пи на радиус в квадрате)'''
    return 2 * math.pi * r

