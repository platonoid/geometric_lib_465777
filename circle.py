import math

'''Импортируем библиотеку математика'''

def area(r):
    '''функция принимает число р и возвращает пи умноженное на р квадрат(площадь)'''

    return math.pi * r * r


def perimeter(r):
    '''функция принимает число r и возвращает 2 умножить на r(периметр)'''

    return 2 * math.pi * r

