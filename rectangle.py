def area(a, b): 
    '''Получает длины сторон прямоугольника a (int) и b (int) и высчитывает площадь этого прямоугольника(произведение двух сторон)'''
    return a * b 

def perimeter(a, b): 
    '''Получает длины сторон прямоугольника a (int) и b (int) и высчитывает периметр этого прямоугольника(удвоенное произведение суммы двух сторон)'''
    return 2 * (a + b) 

