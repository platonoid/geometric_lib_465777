
def area(a, b):
    ''' Принимает 2 числа:
    a - первая сторона прямоугольника,
    b - вторая сторона прямоугольника.
    Возвращает площадь прямоугольника '''
    return a * b


def perimeter(a, b):
    ''' Принимает 2 числа:
    a - первая сторона прямоугольника,
    b - вторая сторона прямоугольника.
    Возвращает периметр прямоугольника '''
    return (a + b)
